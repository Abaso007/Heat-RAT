void Help(string path)
{
	HANDLE  hConsole;
	hConsole = GetStdHandle(STD_OUTPUT_HANDLE);
	FlushConsoleInputBuffer(hConsole);
	int col = 9;
	bool logoShow = true;
	string space = "          ";
	string command = "";
	string line = "";
	string something = "";
	string curentUSER = "";

	col = 8;
	SetConsoleTextAttribute(hConsole, col);
	cout << "\n\n";
	ifstream file(path);
	if (file.is_open())
	{
		while (getline(file, line))
		{
			cout << line << endl;
		}
	}
	else
	{

		col = 4;
		SetConsoleTextAttribute(hConsole, col);
		printf("Fatal ERROR!!!");
	}
	file.close();
	line = "";
	col = 11;
	SetConsoleTextAttribute(hConsole, col);
	cout << "\n\n";
}